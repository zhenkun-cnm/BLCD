#ifndef __BLDC_ADC_H
#define __BLDC_ADC_H

#include <stdint.h>

/* ========================================================================== */
/* 1. 硬件与业务层核心配置宏                                                   */
/* ========================================================================== */
#define ADC_CONVERT_NUM    2   /* 当前硬件物理扫描的 ADC 通道总数 */

/* ========================================================================== */
/* 2. 可扩展的电调模拟量综合业务结构体                                         */
/* 后续若硬件增加采样通道，只需在此结构体末尾追加新变量，函数形参永远不瞎折腾      */
/* ========================================================================== */
typedef struct {
    uint16_t phase_i;    /* 相电流采样原始值 (0 ~ 4095) */
    uint16_t v_bus;      /* 母线电压采样原始值 (0 ~ 4095) */
    
    /* 预留后续改版可随时启用的扩展通道 */
    uint16_t temp_mos;   /* 扩展：MOS管或核心板温度 */
    uint16_t throttle;   /* 扩展：外部油门电位器 */
} BLDC_Metrics_t;

/* ========================================================================== */
/* 3. 全局底层变量与标志位声明（声明为 volatile 防止被编译器错误优化）           */
/* ========================================================================== */
extern volatile uint16_t g_adc_raw_value[ADC_CONVERT_NUM];
extern volatile uint8_t  g_adc_new_data_flag;
extern volatile BLDC_Metrics_t  g_adc_metrics;
/* ========================================================================== */
/* 4. 公共底层驱动及数据同步接口                                               */
/* ========================================================================== */
void    BSP_ADC_Init(void);
void    BSP_ADC_Start(void);
void    BSP_ADC_Stop(void);
int32_t BSP_ADC_Calibration_Start(void);

/* 核心扩展拉取接口：同步死等并自动清除标志位，全自动填充结构体 */
int32_t BSP_ADC_GetMetrics(volatile BLDC_Metrics_t *p_metrics);

#endif /* __BLDC_ADC_H */
